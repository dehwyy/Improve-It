import { ClientSafeProvider, getProviders, LiteralUnion } from 'next-auth/react'
import { getServerSession } from 'next-auth/next'
import { authOptions } from '@/pages/api/auth/[...nextauth]'
import '../../global.css'
import AuthMethodWrapper, { GoogleWrapperForSvg } from '@/app/auth/signin/components/AuthMethodWrapper'
import { BuiltInProviderType } from 'next-auth/providers'
import { redirect } from 'next/navigation'
import { Metadata } from 'next'

export const metadata: Metadata = {
  title: 'Authorization',
}

async function getSession() {
  const session = await getServerSession(authOptions)
  if (session) {
    return { redirect: { destination: '/' } }
  }
  const providers = await getProviders()
  return {
    props: { providers: providers ?? [] },
  }
}
export default async function SignIn() {
  const { props: auth, redirect: authRedirect } = await getSession()
  if (authRedirect) return redirect(authRedirect.destination)
  const { github, vk, google, yandex } = auth.providers as Record<LiteralUnion<BuiltInProviderType, string>, ClientSafeProvider>
  return (
    <div className="w-screen h-screen bg-[#333333] flex items-center justify-center cursor-pointer">
      <div className="w-96 flex flex-col justify-evenly gap-6 p-5 ">
        <GoogleWrapperForSvg>
          <AuthMethodWrapper color="yellow" id={google.id} name={google.name}>
            <GoogleSVG />
          </AuthMethodWrapper>
        </GoogleWrapperForSvg>
        <AuthMethodWrapper color="green" id={github.id} name={github.name}>
          <GithubSVG />
        </AuthMethodWrapper>
        <AuthMethodWrapper color="blue" id={vk.id} name={vk.name}>
          <VkSVG />
        </AuthMethodWrapper>
        <AuthMethodWrapper color="fuchsia" id={yandex.id} name={yandex.name}>
          <YandexSVG />
        </AuthMethodWrapper>
      </div>
    </div>
  )
}

